package com.clicktravel.cheddar.infrastructure.persistence.document.search.sort;

public class SortingOption {

    public enum Direction {
        ASCENDING,
        DESCENDING;
    }

    private final String key;
    private final Direction direction;

    public SortingOption(final String key) {
        this.key = key;
        direction = Direction.ASCENDING;
    }

    public SortingOption(final String key, final Direction direction) {
        this.key = key;
        this.direction = direction;
    }

    public String key() {
        return key;
    }

    public Direction direction() {
        return direction;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((direction == null) ? 0 : direction.hashCode());
        result = prime * result + ((key == null) ? 0 : key.hashCode());
        return result;
    }

    @Override
    public boolean equals(final Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        final SortingOption other = (SortingOption) obj;
        if (direction != other.direction) {
            return false;
        }
        if (key == null) {
            if (other.key != null) {
                return false;
            }
        } else if (!key.equals(other.key)) {
            return false;
        }
        return true;
    }

}